public class USING_DEFAULT {
    public  static  void main(String[]args){
        int month=7;
        switch (month){
            case 1:
            System.out.println("JAN");
            break;
            case 2:
                System.out.println("feb");
                break;
            case 3:
                System.out.println("march");
                break;
            default:
                System.out.println("MAY BE MONTH IN NOT EXIST");

        }
    }


}
